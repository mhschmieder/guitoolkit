package com.mhschmieder.jcontrols.icon;

/**
 * {@code IconConstants} is a container class for constants needed by icons.
 *
 * @version 1.0
 *
 * @author Mark Schmieder
 */
public final class IconConstants {

    /**
     * The default constructor is disabled, as this is a static constants class.
     */
    private IconConstants() {}

    /**
     * The number of pixels to use for an icon hosted by a frame title bar.
     */
    public static final int FRAME_TITLE_ICON_SIZE    = 16;

    /**
     * The number of pixels to offset an icon hosted by a frame title bar.
     */
    public static final int FRAME_TITLE_ICON_INSET   = 2;

    /**
     * The number of pixels to use for an icon hosted by a menu or menu item.
     */
    public static final int MENU_ICON_SIZE           = 16;

    /**
     * The number of pixels to offset an icon hosted by a menu or menu item.
     */
    public static final int MENU_ICON_INSET          = 2;

    /**
     * The number of pixels to use for an icon hosted by a tool bar button.
     */
    public static final int TOOLBAR_ICON_SIZE        = 24;

    /**
     * The number of pixels to offset an icon hosted by a tool bar button.
     */
    public static final int TOOLBAR_ICON_INSET       = 4;

    /**
     * The number of pixels to use for an icon hosted by a control panel.
     */
    public static final int CONTROL_PANEL_ICON_SIZE  = 32;

    /**
     * The number of pixels to offset an icon hosted by a control panel.
     */
    public static final int CONTROL_PANEL_ICON_INSET = 6;

}
